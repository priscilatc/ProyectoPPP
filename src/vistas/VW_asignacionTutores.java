package vistas;

public class VW_asignacionTutores {

	private int idasignacion;
	private String comentario;
	private String estudiante;
	private String tutor_tecnico;
	private String tutor_academico;
	
	public int getIdasignacion() {
		return idasignacion;
	}
	public void setIdasignacion(int idasignacion) {
		this.idasignacion = idasignacion;
	}
	public String getComentario() {
		return comentario;
	}
	public void setComentario(String comentario) {
		this.comentario = comentario;
	}
	public String getEstudiante() {
		return estudiante;
	}
	public void setEstudiante(String estudiante) {
		this.estudiante = estudiante;
	}
	public String getTutor_tecnico() {
		return tutor_tecnico;
	}
	public void setTutor_tecnico(String tutor_tecnico) {
		this.tutor_tecnico = tutor_tecnico;
	}
	public String getTutor_academico() {
		return tutor_academico;
	}
	public void setTutor_academico(String tutor_academico) {
		this.tutor_academico = tutor_academico;
	}
	
}
