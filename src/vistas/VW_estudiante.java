package vistas;

public class VW_estudiante {
	private int idestudiante;
	private String nombre;
	private String correo;
	private String celular;
	private String condicion;
	private String sexo;
	private String coordinacion;
	private String usuario;
	private String carneuca;
	
	public int getIdestudiante() {
		return idestudiante;
	}
	public void setIdestudiante(int idestudiante) {
		this.idestudiante = idestudiante;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getCondicion() {
		return condicion;
	}
	public void setCondicion(String condicion) {
		this.condicion = condicion;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getCoordinacion() {
		return coordinacion;
	}
	public void setCoordinacion(String coordinacion) {
		this.coordinacion = coordinacion;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getCarneuca() {
		return carneuca;
	}
	public void setCarneuca(String carneuca) {
		this.carneuca = carneuca;
	}
	
}
